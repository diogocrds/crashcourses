const os = require('os');

// platform = Platform
console.log(os.platform());

// arch = CPU Architechture
console.log(os.arch());

// cpus = CPU Core Info
console.log(os.cpus());

// freemem = Free memory
console.log(os.freemem());

// totalmem = Total memory
console.log(os.totalmem());

// homedir = Home directory
console.log(os.homedir());

// uptime = Uptime
console.log(os.uptime());
